use std::fs;
use std::path::Path;
use std::fmt::Write;

fn main() {
    let out_dir = std::env::var("OUT_DIR").unwrap();
    let dest = Path::new(&out_dir).join("font_data.rs");

    let mut glyphs = vec![[0u8; 16]; 256];
    generate_vga_font(&mut glyphs);

    let mut out = String::from(
        "pub static FONT8X16: &[u8; 4096] = &[\n"
    );

    for g in &glyphs {
        write!(out, "    ").unwrap();
        for &b in g {
            write!(out, "0x{:02x},", b).unwrap();
        }
        writeln!(out).unwrap();
    }
    out.push_str("];\n");

    fs::write(&dest, out).unwrap();
    println!("cargo:rerun-if-changed=build.rs");
}

fn add_glyph(glyphs: &mut [[u8; 16]], ch: u8, rows: &[u8]) {
    if ch as usize >= 256 { return; }
    for (i, &row) in rows.iter().enumerate() {
        if i < 16 {
            glyphs[ch as usize][i] = row;
        }
    }
}

